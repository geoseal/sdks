// Builds www/checkpoint.js — the single Cordova js-module — from src/index.ts.
// esbuild inlines '@geoseal/capacitor/core' (the Capacitor-free REST/presence
// layer) so the shipped module is self-contained and single-sourced; only
// 'cordova/exec' stays external (provided by cordova.js at runtime).
import { build } from "esbuild";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const coreVersion = require("@geoseal/capacitor/package.json").version;

await build({
  entryPoints: ["src/index.ts"],
  bundle: true,
  format: "cjs",
  platform: "browser",
  target: "es2020",
  external: ["cordova/exec"],
  outfile: "www/checkpoint.js",
  banner: {
    js: [
      "// GENERATED by 'npm run build' (scripts/bundle.mjs, esbuild) — do not edit by hand.",
      `// Source: src/index.ts, single-sourcing the REST/presence layer from @geoseal/capacitor/core@${coreVersion}.`,
    ].join("\n"),
  },
  footer: {
    js: [
      "// Cordova global shape: window.Checkpoint = the Checkpoint facade methods at the",
      "// top level + every named export (NativeGeofence, REST/presence helpers) namespaced.",
      "module.exports = Object.assign({}, module.exports, module.exports.Checkpoint);",
    ].join("\n"),
  },
});
